var colors = ["white", "black", "red", "orange", "green", "blue", "indigo", "violet"];

//console log different colors

console.log(colors[0]);
console.log(colors[5]);

//add an item to colors **goes to the end**
console.log(colors);
colors.push('pink');
console.log(colors);

//delete an item from colors **deletes the last item**

console.log(colors);
colors.pop();
console.log(colors);
